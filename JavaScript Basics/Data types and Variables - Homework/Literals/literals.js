// Array literal
var arrayLiteral = new Array(2);

// Integer literal in decimal base
var integerLiteralInDecimalBase = 1;

// Integer literal in hexadecimal base
var integerLiteralInHexadecimalBase = 0x12;

// Integer literal in octal base
var integerLiteralInOctalBase = 018;

// Floating literal.
var floatingLiteral = 1.1;

// Boolean literal.
var booleanLiteral = true;

// Object literal
var objectLiteral = {
	name: "Telerik"
};

// String literal with single quotes
var stringLiteralWithSingleQuotes = 'Single quotes.';

// String literal with double quotes
var stringLiteralWithDoubleQuotes = "Double quotes.";

// String interpolation
var name = 'Telerik Academy';
var stringInterpolation = `${name} is great.`;

// Check the type of variables data.
console.log(typeof(arrayLiteral));
console.log(typeof(integerLiteralInDecimalBase));
